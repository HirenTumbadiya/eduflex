const mongoose = require('mongoose');

// Define the Workshop schema
const workshopSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  instructor: {
    type: String,
    required: true,
  },
  startDate: {
    type: Date,
    required: true,
  },
  endDate: {
    type: Date,
    required: true,
  },
});


// Create the Workshop model
const Workshop = mongoose.model('Workshop', workshopSchema);

module.exports = Workshop;
